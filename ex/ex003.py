# Number
x = 10
y = 1.5
z = -1

# Addition
print("Addition")
print(x + y)
print(y + z)
print(x + z)

# Subtraction
print("Subtraction")
print(x - y)
print(y - z)
print(x - z)

# Multiplication
print("Multiplication")
print(x * y)
print(y * z)
print(x * z)

# Devision
print("Devision")
print(x / y)
print(y / z)
print(x / z)

# Square
print("Square")
print(x ** 2)
print(y ** 2)
print(x ** 2)

# Modular
print("Modular")
print(x % 2)
print(y % 2)
print(x % 2)
